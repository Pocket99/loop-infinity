def clean_quotes(shipping_data):
    cleaned_data = {}
    for country, rates in shipping_data.items():
        cleaned_rates = []
        for rate in rates:
            cleaned_rates.append(tuple(float(value) if isinstance(value, str) and value.replace('.', '', 1).isdigit() else value for value in rate))
        cleaned_data[country] = cleaned_rates
    return cleaned_data

